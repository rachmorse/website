    <p>Breast cancer is the most common cause of cancer-related death in Peru, and its treatment is physically and
        emotionally demanding. In this study, we wanted to understand how women in Peru cope with breast cancer
        treatment and what support they need. </p>
    <p>We found that women lacked information about breast cancer and its treatment and wanted more guidance from health
        professionals. Women also experienced many challenging emotions that could be supported by social support
        groups; however, not all women had access to these groups, and instead relied on friends and family for support.
        We suggest that breast cancer programs provide more informational support to help women cope with breast cancer
        treatment.</p>
    
