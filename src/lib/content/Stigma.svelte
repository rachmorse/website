    <p>HPV is a common sexually transmitted disease that can develop into cervical cancer. Most women will get an HPV
        infection in their lifetime that their bodies will be able control. However, in some cases, women will need
        treatment to help prevent HPV from developing into cervical cancer. An HPV infection can come from any past
        sexual partner, even if a woman has been in a monogamous relationship for a long time. Despite how common HPV
        is, HPV and cervical cancer are often stigmatized. </p>
    <p>In this study, we aimed to understand this stigma and explore its impact. We conducted our research in a
        low-resource setting in Peru and found widespread stigma where HPV was considered a shameful and secretive
        disease. The stigma blamed women for being promiscuous or men for being unfaithful, and was reinforced by
        patients, community members, healthcare workers and policies. This stigma sometimes stopped women from getting
        screened for HPV. </p>
    <p>We propose that cervical cancer prevention programs address stigma throughout the healthcare system and, instead
        of focusing on associations between HPV and sexual behaviors, provide comprehensive information about HPV and
        offer screening to all eligible women, regardless of sexual history.</p>
